        let currentWonder = 'taj-mahal';
        let currentPhotoIndex = 0;
        
        function showDestination(wonderKey) {
            if (wonderKey === currentWonder) return;
            
            const wonder = wondersData[wonderKey];
            if (!wonder) return;
            
            currentWonder = wonderKey;
            currentPhotoIndex = 0;
            
            // Update active state in cards
            document.querySelectorAll('.destination-card').forEach(card => {
                card.classList.remove('active');
            });
            event.target.closest('.destination-card').classList.add('active');
            
            // Update hero section with smooth transition
            const heroSection = document.getElementById('heroSection');
            const locationBadge = document.getElementById('locationBadge');
            const heroTitle = document.getElementById('heroTitle');
            const heroDescription = document.getElementById('heroDescription');
            
            // Fade out content
            heroSection.style.opacity = '0.7';
            
            setTimeout(() => {
                // Update background
                heroSection.style.backgroundImage = `url('${wonder.background}')`;
                
                // Update content
                locationBadge.innerHTML = `<i class="fas fa-map-marker-alt me-2"></i>${wonder.location}`;
                heroTitle.textContent = wonder.title;
                heroDescription.textContent = wonder.description;
                
                // Fade back in
                heroSection.style.opacity = '1';
            }, 400);
        }
        
        function showNextPhoto() {
            const gallery = document.getElementById('photoGallery');
            gallery.classList.add('active');
            updatePhoto();
        }
        
        function closeGallery() {
            const gallery = document.getElementById('photoGallery');
            gallery.classList.remove('active');
        }
        
        function nextPhoto() {
            const currentWonderData = wondersData[currentWonder];
            currentPhotoIndex = (currentPhotoIndex + 1) % currentWonderData.photos.length;
            updatePhoto();
        }
        
        function prevPhoto() {
            const currentWonderData = wondersData[currentWonder];
            currentPhotoIndex = (currentPhotoIndex - 1 + currentWonderData.photos.length) % currentWonderData.photos.length;
            updatePhoto();
        }
        
        function updatePhoto() {
            const currentWonderData = wondersData[currentWonder];
            const photo = currentWonderData.photos[currentPhotoIndex];
            
            document.getElementById('galleryImage').src = photo.url;
            document.getElementById('photoTitle').textContent = photo.title;
            document.getElementById('photoDescription').textContent = photo.description;
            document.getElementById('currentPhoto').textContent = currentPhotoIndex + 1;
            document.getElementById('totalPhotos').textContent = currentWonderData.photos.length;
            
            // Update navigation buttons
            const prevBtn = document.querySelector('.gallery-nav.prev');
            const nextBtn = document.querySelector('.gallery-nav.next');
            
            prevBtn.disabled = currentPhotoIndex === 0;
            nextBtn.disabled = currentPhotoIndex === currentWonderData.photos.length - 1;
        }
        
        // Close gallery when clicking outside
        document.getElementById('photoGallery').addEventListener('click', function(e) {
            if (e.target === this) {
                closeGallery();
            }
        });
        
        // Keyboard navigation
        document.addEventListener('keydown', function(e) {
            const gallery = document.getElementById('photoGallery');
            if (gallery.classList.contains('active')) {
                if (e.key === 'ArrowRight') nextPhoto();
                if (e.key === 'ArrowLeft') prevPhoto();
                if (e.key === 'Escape') closeGallery();
            }
        });
        
        // Initialize page
        document.addEventListener('DOMContentLoaded', function() {
            // Set initial active state
            showDestination('taj-mahal');
        });
